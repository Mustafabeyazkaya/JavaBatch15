package class2;

public class OperatorsDemo {
    public static void main(String[] args) {
        int number1=10;
        int number2=20;
        System.out.println(number1+number2);
        System.out.println(number1-number2);
        System.out.println(number1*number2);
        System.out.println(number1/number2);
        // put your sursor anywhere on a line and press Ctrl+D to duplicate it
        /* if u have to store whole numbers, always use int
        if u have to store decimal numbers, always use double
        if u have to store characters you can always use String
         */
        float m2=10.5f;
        float m3=20.8f;
        System.out.println(m2/m3);

    }
}
