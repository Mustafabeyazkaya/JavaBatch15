package class7;

public class ForLoops {
    public static void main(String[] args) {
        /*
        prints number of 10 to 20
         */
        int number=0;
        while (number<10){
            System.out.println(number);
            number++;

        }
        // or we can use to write code which is standing above, we can use the for loops as below

        /*
        ( initialize: condition: incrementORdecrement)
         */
        for (int i=0;i<10;i++){
            System.out.println(i);
        }
    }
}
